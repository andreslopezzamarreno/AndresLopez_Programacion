public class CamionCalabazas {
    public static void main(String args[]) {

        // variables
        int caja = 8;
        int camion1 = 3464;
        int camion2 = camion1 - 664;
        int camion3 = 220;

        // Sacar por consola los datos
        // operaciones dentro de la salida
        System.out.println("El camion 1 lleva " + camion1 / caja + " kilos");
        System.out.println("El camion 2 lleva " + camion2 / caja );
        System.out.println("El camion 3 lleva " + camion3 * caja );
    }
}
